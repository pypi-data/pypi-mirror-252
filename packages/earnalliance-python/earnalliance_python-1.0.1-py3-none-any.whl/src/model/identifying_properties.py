class IdentifyingProperties:
    def __init__(
        self,
        appleId=None,
        discordId=None,
        email=None,
        epicGamesId=None,
        steamId=None,
        twitterId=None,
        walletAddress=None,
    ) -> None:
        self.appleId = appleId
        self.discordId = discordId
        self.email = email
        self.epicGamesId = epicGamesId
        self.steamId = steamId
        self.twitterId = twitterId
        self.walletAddress = walletAddress
